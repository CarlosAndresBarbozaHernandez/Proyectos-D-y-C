public class ejercicio_03 {
    /**
     * 5. Bucles
     *
     * 3. Muestra los numeros multiplos de 5, de 0 a 100 utilizando un bucle do-while.
     *
     * @author Carlos Barboza
     */
    public static void main(String[] args) {

        int numero = 5;

        do {
            System.out.println(numero);
            numero += 5;

        } while (numero < 105);
    }
}
