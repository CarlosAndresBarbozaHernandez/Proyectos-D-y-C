/**
 * 5. Bucles
 *
 * 5. Muestra los numeros del 320 al 160, contando de 20 en 20 hacia atras utilizando un bucle
 * while.
 *
 * @author Carlos Barboza
 */
public class ejercicio_05 {
    public static void main(String[] args) {

        int numero = 320;

        while (numero >= 160){

            System.out.println(numero);
            numero -= 20;
        }
    }
}
